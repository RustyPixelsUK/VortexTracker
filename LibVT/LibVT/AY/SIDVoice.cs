        public void ScaleWaveform(byte[] ampTable, byte[] dst, byte[] src, int dstOffset, int srcOffset)
        {
            for (int i = 31; i >= 0; i--)
            {
                byte sample = src[srcOffset + i];
                byte scaled = ampTable[sample];
                dst[dstOffset + i] = scaled;
            }
        }

        public ushort ClockByPeriod(uint clock, ushort period, byte shift)
        {
            if (period == 0) period = 1;
            uint scaledPeriod = (uint)(period << shift);
            return (ushort)((_timerRate << 8) / (clock / scaledPeriod));
        }

