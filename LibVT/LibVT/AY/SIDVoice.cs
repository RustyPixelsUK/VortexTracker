        // Update at 15kHz
        public void InterruptTick(AYRegisters regs)
        {
            if (--_50hzCount == 0)
            {
                _50hzCount = _50hzReset;
                Rout(regs);
            }

            if (_mode.HasFlag(SidMode.SIDA))
            {
                _tickA++;
                if (_tickA >= _resetA)
                {
                    _tickA = 0;
                    _posA++;
                    if (_posA >= _compareA)
                        _posA = 0;
                }

                regs.AmplitudeA = _waveformA[_posA];
            }

            if (_mode.HasFlag(SidMode.SIDB))
            {
                _tickB++;
                if (_tickB >= _resetB)
                {
                    _tickB = 0;
                    _posB++;
                    if (_posB >= _compareB)
                        _posB = 0;
                }

                regs.AmplitudeB = _waveformB[_posB];
            }

            if (_mode.HasFlag(SidMode.SIDC))
            {
                _tickC++;
                if (_tickC >= _resetC)
                {
                    _tickC = 0;
                    _posC++;
                    if (_posC >= _compareC)
                        _posC = 0;
                }

                regs.AmplitudeC = _waveformC[_posC];
            }
        }

        // Update at 50Hz
        public void Rout(AYRegisters regs)
        {
            if (_mode.HasFlag(SidMode.SIDA))
            {
                _compareA = _waveLenA;
                ScaleWaveform(_prescaleTab, _waveformA, _waveformA, 0, 0);

                ushort newPeriod = regs.ToneA;
                if (newPeriod != _periodA)
                {
                    _periodA = newPeriod;
                    ushort timerVal = ClockByPeriod(_psgClockA, newPeriod, _shiftA);
                    _tickA = (ushort)(timerVal + _detuneA);
                    _resetA = _tickA;
                }
            }

            if (_mode.HasFlag(SidMode.SIDB))
            {
                _compareB = _waveLenB;
                ScaleWaveform(_prescaleTab, _waveformB, _waveformB, 0, 0);

                ushort newPeriod = regs.ToneB;
                if (newPeriod != _periodB)
                {
                    _periodB = newPeriod;
                    ushort timerVal = ClockByPeriod(_psgClockB, newPeriod, _shiftB);
                    _tickB = (ushort)(timerVal + _detuneB);
                    _resetB = _tickB;
                }
            }

            if (_mode.HasFlag(SidMode.SIDC))
            {
                _compareC = _waveLenC;
                ScaleWaveform(_prescaleTab, _waveformC, _waveformC, 0, 0);

                ushort newPeriod = regs.ToneC;
                if (newPeriod != _periodC)
                {
                    _periodC = newPeriod;
                    ushort timerVal = ClockByPeriod(_psgClockC, newPeriod, _shiftC);
                    _tickC = (ushort)(timerVal + _detuneC);
                    _resetC = _tickC;
                }
            }
        }

